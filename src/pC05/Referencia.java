package pC05;

public class Referencia {
    public int x;

    public Referencia() {
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return this.x;
    }

    public String toString() {
        return "Referencia{" +
                "x=" + x +
                '}';
    }
}
